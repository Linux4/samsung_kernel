/**
 * This function is called when an lm_device is bound to a
 * dwc_otg_driver. It creates the driver components required to
 * control the device (CIL, HCD, and PCD) and it initializes the
 * device. The driver components are stored in a dwc_otg_device
 * structure. A reference to the dwc_otg_device is saved in the
 * lm_device. This allows the driver to access the dwc_otg_device
 * structure on subsequent calls to driver methods for this device.
 *
 * @param[in] _dev Bus device
 */
 int   ldo_gpio =0xffffffff ;
static int dwc_otg_driver_probe(
	struct platform_device *_dev
)
{
	int retval = 0;
	dwc_otg_device_t *dwc_otg_device;
	int irq;
#ifndef CONFIG_OF
	struct sprd_usb_platform_data *pdata= _dev->dev.platform_data;
#else
	int ret;
	struct device_node *np = _dev->dev.of_node;
	struct resource res;

	if (np)
		_dev->id = of_alias_get_id(np, "usb");

	ret = of_address_to_resource(np, 0, &res);
	if(ret < 0){
		dev_err(&_dev->dev, "no reg of property specified\n");
		return NULL;
	}
#endif
	usb_phy_init(_dev);

	dev_dbg(&_dev->dev, "dwc_otg_driver_probe(%p)\n", _dev);

	dwc_otg_device = dwc_alloc(sizeof(dwc_otg_device_t));

	if (!dwc_otg_device) {
		dev_err(&_dev->dev, "kmalloc of dwc_otg_device failed\n");
		retval = -ENOMEM;
		goto fail;
	}

	memset(dwc_otg_device, 0, sizeof(*dwc_otg_device));
	dwc_otg_device->os_dep.reg_offset = 0xFFFFFFFF;

	/*
	 * Map the DWC_otg Core memory into virtual address space.
	 */
#ifdef CONFIG_OF
	dwc_otg_device->os_dep.base = res.start;
#else
	dwc_otg_device->os_dep.base = (void *)platform_get_resource(_dev,
						IORESOURCE_MEM, 0)->start;
#endif
	if (!dwc_otg_device->os_dep.base) {
		dev_err(&_dev->dev, "ioremap() failed\n");
		retval = -ENOMEM;
		goto fail;
	}
	dev_dbg(&_dev->dev, "base=0x%08x\n", (unsigned)dwc_otg_device->os_dep.base);

	irq = platform_get_irq(_dev, 0);
	dev_dbg(&_dev->dev, "base=0x%08x irq:%d\n", (unsigned)dwc_otg_device->os_dep.base,
			irq);
	/*
	 * Initialize driver data to point to the global DWC_otg
	 * Device structure.
	 */
#ifdef CONFIG_OF
	if(np){
		struct sprd_usb_platform_data *pdata;
		pdata = __DWC_ALLOC(NULL,sizeof(struct sprd_usb_platform_data));
		if(!pdata){
			dev_err(&_dev->dev, "fail to malloc memory for platform_data\n");
			return -ENOMEM;
		}
		if (of_property_read_u32(np, "ngpios", &pdata->gpio_num))
		{
			pr_info("read gpio number error\n");
			__DWC_FREE(NULL , pdata);
			return -ENODEV;
		}
		if (1 == pdata->gpio_num)
		{
			pdata->gpio_chgdet = of_get_gpio(np,  0);
			if(pdata->gpio_chgdet<0){
				printk(" get charge detect  error ,gpio is %d\n",pdata->gpio_chgdet);
				pdata->gpio_chgdet = 0xffffffff;
			}

			pdata->gpio_otgdet = 0xffffffff;
			pdata->gpio_boost = 0xffffffff;
		 }else if(2 == pdata->gpio_num){
			pdata->gpio_chgdet = of_get_gpio(np,  0);
			if(pdata->gpio_chgdet<0){
				printk(" get charge detect  error ,gpio is %d\n",pdata->gpio_chgdet);
				pdata->gpio_chgdet = 0xffffffff;
			}

			pdata->gpio_otgdet = of_get_gpio(np,  1);
			if(pdata->gpio_otgdet<0){
				printk(" get otg cable detect  error ,gpio is %d\n",pdata->gpio_otgdet);
				pdata->gpio_otgdet = 0xffffffff;
			}

			pdata->gpio_boost = 0xffffffff;
		}else if(3 == pdata->gpio_num){
			pdata->gpio_chgdet = of_get_gpio(np,  0);
			if(pdata->gpio_chgdet<0){
			printk(" get charge detect  error ,gpio is %d\n",pdata->gpio_chgdet);
			pdata->gpio_chgdet = 0xffffffff;
			}
			pdata->gpio_otgdet = of_get_gpio(np,  1);
			if(pdata->gpio_otgdet<0){
			printk(" get otg cable detect  error ,gpio is %d\n",pdata->gpio_otgdet);
			pdata->gpio_otgdet = 0xffffffff;
			}

			pdata->ldo_gpio = of_get_gpio(np,  2);
			if(pdata->ldo_gpio<0){
				printk("get ldo  error ,gpio is  %d\n",pdata->ldo_gpio);
				pdata->ldo_gpio = 0xffffffff;
			}
			pdata->gpio_otgdet = 0xffffffff;
			ldo_gpio= pdata->ldo_gpio;
		}else{
			printk("gpio number more than three,gpio number  %d\n",pdata->gpio_num);
		}

		_dev->dev.platform_data = (void *)pdata;
		/*
		Prevent checking defect fix by SRCN Ju Huaiwei(huaiwei.ju) Aug.27th.2014
		http://10.252.250.112:7010  => [CXX]Galaxy-Core-Prime IN INDIA OPEN
		CID 28620 (#1 of 1): Wrong sizeof argument (SIZEOF_MISMATCH)suspicious_sizeof:
		Passing argument &dwc_otg_device->platform_data of type struct sprd_usb_platform_data
		 and argument 4U  sizeof (pdata)  to function memcpy is suspicious.
             memcpy(&dwc_otg_device->platform_data,pdata,sizeof(pdata));
		*/
		memcpy(&dwc_otg_device->platform_data,pdata,sizeof(struct sprd_usb_platform_data));
		}
#else
	memcpy(&dwc_otg_device->platform_data,pdata,sizeof(struct sprd_usb_platform_data));
#endif
	platform_set_drvdata(_dev, dwc_otg_device);

	dev_dbg(&_dev->dev, "dwc_otg_device=0x%p\n", dwc_otg_device);

	udc_enable();
	dwc_otg_device->core_if = dwc_otg_cil_init(dwc_otg_device->os_dep.base);
	if (!dwc_otg_device->core_if) {
		dev_err(&_dev->dev, "CIL initialization failed!\n");
		retval = -ENOMEM;
		goto fail;
	}
	/*
	 * Attempt to ensure this device is really a DWC_otg Controller.
	 * Read and verify the SNPSID register contents. The value should be
	 * 0x45F42XXX, which corresponds to "OT2", as in "OTG version 2.XX".
	 */

	if ((dwc_otg_get_gsnpsid(dwc_otg_device->core_if) & 0xFFFFF000) !=
	    0x4F542000) {
		dev_err(&_dev->dev, "Bad value for SNPSID: 0x%08x\n",
			dwc_otg_get_gsnpsid(dwc_otg_device->core_if));
		dwc_otg_cil_remove(dwc_otg_device->core_if);
		dwc_free(dwc_otg_device);
		retval = -EINVAL;
		goto fail;
	}

	/*
	 * Validate parameter values.
	 */
	if (set_parameters(dwc_otg_device->core_if)) {
		dwc_otg_cil_remove(dwc_otg_device->core_if);
		retval = -EINVAL;
		goto fail;
	}

	/*
	 * Create Device Attributes in sysfs
	 */
	dwc_otg_attr_create(_dev);

{
	dev_dbg(&_dev->dev, "number EP=%d\n", dwc_otg_get_param_dev_endpoints(dwc_otg_device->core_if));
}
	/*
	 * Disable the global interrupt until all the interrupt
	 * handlers are installed.
	 */
	dwc_otg_disable_global_interrupts(dwc_otg_device->core_if);

	/*
	 * Install the interrupt handler for the common interrupts before
	 * enabling common interrupts in core_init below.
	 */
	DWC_DEBUGPL(DBG_CIL, "registering (common) handler for irq%d\n",
		    irq);

	/*
	 *no need to use comm_irq if the usb IP core can work only in device mode
	 *but if support otg, we should use common_irq
	 *such as device disconnect when usb cable is connected,plug out udisk
	 */
	retval = request_irq(irq, dwc_otg_common_irq,
			     IRQF_SHARED, "dwc_otg", dwc_otg_device);
			     //SA_SHIRQ, "dwc_otg", dwc_otg_device);
	if (retval) {
		DWC_ERROR("request of irq%d failed\n", irq);
		retval = -EBUSY;
		goto fail;
	} else {
		dwc_otg_device->common_irq_installed = 1;
	}

#if 0//ndef DWC_DEVICE_ONLY //no need
	/*
	 * judge wheather otg cable is connected 
	 */
	if(!usb_get_id_state())
		dwc_otg_core_fore_host(dwc_otg_device->core_if);
#endif
	/*
	 * Initialize the DWC_otg core.
	 */
	dwc_otg_core_init(dwc_otg_device->core_if);

#ifndef DWC_HOST_ONLY
	/*
	 * Initialize the PCD
	 */
	retval = pcd_init(_dev);
	if (retval != 0) {
		DWC_ERROR("pcd_init failed\n");
		dwc_otg_device->pcd = NULL;
		goto fail;
	}
#endif
#ifndef DWC_DEVICE_ONLY
	/*
	 * Initialize the HCD
	 */
	retval = hcd_init(_dev);
	if (retval != 0) {
		DWC_ERROR("hcd_init failed\n");
		dwc_otg_device->hcd = NULL;
		goto fail;
	}
#endif

	/*Recover from drvdata having been overwritten by hcd_init()*/
	platform_set_drvdata(_dev, dwc_otg_device);
	dwc_otg_device->os_dep.platformdev = _dev;

	/*
	 * Enable the global interrupt after all the interrupt
	 * handlers are installed.
	 */
	//dwc_otg_enable_global_interrupts(dwc_otg_device->core_if);

	return 0;

fail:
	dwc_otg_driver_remove(_dev);
	return retval;
}

