static int mtk_vcp_is_ready(void)
{
	u32 val = 0;

	val = get_mt6989_reg_value(vlp_ao, 0x91c);

	if ((val & (VCP_READY_STA | VCP_VOTE_READY_STA)) == (VCP_READY_STA | VCP_VOTE_READY_STA))
		return 1;

	return 0;
}

